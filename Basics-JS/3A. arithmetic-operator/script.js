let number1 = 10,
    number2 = 3;

// Addition
console.log (`${number1} + ${number2} =`, number1 + number2);

// Subtraction
console.log (`${number1} - ${number2} =`, number1 - number2);

// Multiplication
console.log (`${number1} x ${number2} =`, number1 * number2);

// Division
console.log (`${number1} / ${number2} =`, number1 / number2);

// Exponent
console.log (`${number1} ^ ${number2} =`, number1 ** number2);

// Modulus
console.log (`${number1} % ${number2} =`, number1 % number2);

let before = number1;

// Increment
console.log (`${before} + 1 =`, ++number1);

// Decrement
console.log (`${before} - 1 =`, --before);
