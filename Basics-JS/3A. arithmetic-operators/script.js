let number1 = 10;
let number2 = 3;

// Addition
console.log (
    number1 + " + " + number2 + " =", 
    number1 + number2
);

// Subtraction
console.log (
    number1 + " - " + number2 + " =", 
    number1 - number2
);

// Multiplication
console.log (
    number1 + " x " + number2 + " =", 
    number1 * number2
);

// Division
console.log (
    number1 + " / " + number2 + " =", 
    number1 / number2
);

// Exponent
console.log (
    number1 + " ^ " + number2 + " =", 
    number1 ** number2
);

// Modulus
console.log (
    number1 + " % " + number2 + " =", 
    number1 % number2
);

// Increment 
let before = number1;
let result = ++number1;

console.log (
    before + " + " + 1 + " =", 
    result
);

// Decrement
before = number1;
result = --number1;

console.log (
    number1 + " - " + 1 + " =", 
    --number1
);




