let number = 10;

// Addition Assignment
console.log(`${number} + 5 =`, number += 5);

// Subtraction Assignment
console.log(`${number} - 5 =`, number -= 5);

// Multiplication Assignment
console.log(`${number} x 5 =`, number *= 5);

// Division Assignment
console.log(`${number} / 5 =`, number /= 5);

// Exponent Assignment
console.log(`${number} ^ 5 =`, number **= 5);

// Modulus Assignment
console.log(`${number} % 5 =`, number %= 5);
